package com.beans;

public class HospitalBean {
private String HospitalPassword, HospitalName, HospitalID, Mobile, Country, City, Pincode, Address;

public String getHospitalPassword() {
	return HospitalPassword;
}

public void setHospitalPassword(String hospitalPassword) {
	HospitalPassword = hospitalPassword;
}

public String getHospitalName() {
	return HospitalName;
}

public void setHospitalName(String hospitalName) {
	HospitalName = hospitalName;
}

public String getHospitalID() {
	return HospitalID;
}

public void setHospitalID(String hospitalID) {
	HospitalID = hospitalID;
}

public String getMobile() {
	return Mobile;
}

public void setMobile(String mobile) {
	Mobile = mobile;
}

public String getCountry() {
	return Country;
}

public void setCountry(String country) {
	Country = country;
}

public String getCity() {
	return City;
}

public void setCity(String city) {
	City = city;
}

public String getPincode() {
	return Pincode;
}

public void setPincode(String pincode) {
	Pincode = pincode;
}

public String getAddress() {
	return Address;
}

public void setAddress(String address) {
	Address = address;
}
}
